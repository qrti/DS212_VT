/*******************************************************************************
 Draw an empty rectangle
*******************************************************************************/
void Draw_RECT(u16 Col, u16 Posi_x, u16 Posi_y, u16 High, u16 width, u8 R )
{
  u16 x,y,i,j;
  x=Posi_x;
  y=Posi_y;
  SetPosi(x, y);
  for(i=0;i<R;i++){
    SetPosi(x++, y);
    for(j=0; j<High; j++)SetPixel(Col);
  }
  SetPosi(Posi_x+R, y);
  for(i=0;i<width-2*R;i++){
    SetPosi(x, y);
    for(j=0; j<R; j++)SetPixel(Col);
    SetPosi(x++, y+High-R);
    for(j=0; j<R; j++)SetPixel(Col);
  }
  x=Posi_x+width-R;
  SetPosi(x, y);
  for(i=0;i<R;i++){
    SetPosi(x++, y);
    for(j=0; j<High; j++)SetPixel(Col);
  }
}


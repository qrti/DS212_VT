/*******************************************************************************
 Display_Str: Display string at specified position Input: X, Y coordinate, color value, string
*******************************************************************************/
u16 Get_TAB_6x8(u8 Code, u16 Row)
{
  if((Code < 0x21)||(Code > 0x7E))  return 0;
  else if(Row < 6) return CHAR6x8[((Code-0X21)*6)+Row];
  else             return 0;
}

